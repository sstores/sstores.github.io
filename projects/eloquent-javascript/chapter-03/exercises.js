function isEven(num) {
  //base cases
  if (num === 1){ //one is odd
    return false;
  }else if (num === 0){ //zero is even
    return true;
  //recursive?
  }else if (num < 0){ //if its negative make it positive and run it. 
    return isEven(-num);
  }
  return isEven(num - 2);//recur subtracting 2 until num is 0 or 1
  
}

